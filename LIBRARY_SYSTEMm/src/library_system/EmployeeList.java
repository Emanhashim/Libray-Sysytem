private void insertemployee() {
        DefaultTableModel facultyTblModel = (DefaultTableModel) tblemplist.getModel();
        if (btnNew.getText().equalsIgnoreCase("New")) {
            int emptrow = facultyTblModel.getRowCount() + 1;
            facultyTblModel.setRowCount(emptrow);
            btnNew.setText("Save");
        } else {
            int newrow = facultyTblModel.getRowCount()-1;
            try {
                rst.moveToInsertRow();
                rst.updateString("empid", facultyTblModel.getValueAt(newrow, 1).toString());
                rst.updateString("empname", facultyTblModel.getValueAt(newrow, 2).toString());
                rst.updateString("phone", facultyTblModel.getValueAt(newrow, 3).toString());
                rst.updateString("salary", facultyTblModel.getValueAt(newrow, 4).toString());
//               
                rst.insertRow();
                rst.moveToCurrentRow();
                JOptionPane.showMessageDialog(this, "Record saved successfully.");
                btnNew.setText("New");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Database server error" + e.getMessage());
            }
        }
    }
private void updateEmployee() {
        DefaultTableModel facultyTblModel = (DefaultTableModel) tblemplist.getModel();
        try {
            int resp = JOptionPane.showConfirmDialog(this, "Are you sure you want to update the selected record?");
            if (resp == JOptionPane.YES_OPTION) {
                int row = tblemplist.getSelectedRow() + 1;
                rst.absolute(row);
                rst.updateString("empid", facultyTblModel.getValueAt(tblemplist.getSelectedRow(), 1).toString());
                rst.updateString("empname", facultyTblModel.getValueAt(tblemplist.getSelectedRow(), 2).toString());
                rst.updateString("phone", facultyTblModel.getValueAt(tblemplist.getSelectedRow(), 3).toString());
                rst.updateString("salary", facultyTblModel.getValueAt(tblemplist.getSelectedRow(), 4).toString());
                rst.updateRow();
                JOptionPane.showMessageDialog(this, "Record updated successfully.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Database server error" );
        }
        
}
private void deleteFaculty(){
        try {
            int resp = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete the selected record?");
            if (resp == JOptionPane.YES_OPTION) {
                int row = tblemplist.getSelectedRow() + 1;
                rst.absolute(row);
                rst.deleteRow();
                JOptionPane.showMessageDialog(this, "Record deleted successfully.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Database server error" + e.getMessage());
        }
    }






