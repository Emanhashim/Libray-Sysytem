    private void readFaculty() {
       
        DefaultTableModel facultyTblModel = (DefaultTableModel) tblemplist.getModel();
        facultyTblModel.setRowCount(0);
        String sql = "Select * From Employee order by empname ASC";
        try {
            conn = Conection.getConn();
            stmt = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            rst = stmt.executeQuery(sql);
            int row = 0;
            while (rst.next()) {
                facultyTblModel.addRow(new Object[]{rst.getString("empid"),rst.getString("empname"), rst.getString("phone"), rst.getString("salary")});
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database server error" + e.getMessage());
        } finally {
        }

    }
